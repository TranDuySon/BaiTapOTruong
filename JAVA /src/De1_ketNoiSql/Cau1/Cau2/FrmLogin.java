    private void login() {

        try {
            Class.forName("com.mysql.jdbc.Driver");
            String url = "jdbc:mysql://localhost/ktjava?useUnicode=true&characterSet=UTF-8";
            conn = DriverManager.getConnection(url, "root", "root");

            String sql = "SELECT * FROM user WHERE username=? AND password=? LIMIT 1 ";
            PreparedStatement pstmt = conn.prepareStatement(sql);
            pstmt.setString(1, txtUsername.getText());
            pstmt.setString(2, txtPassword.getText());

            ResultSet rs = pstmt.executeQuery();
            if (rs.next()) {
                FrmChonHanhDong hd = new FrmChonHanhDong();
                hd.setVisible(true);
            } else {
                FrmAlert alert = new FrmAlert("User không tồn tại !", "Đăng nhập sai ");
                alert.setVisible(true);
            }
        } catch (ClassNotFoundException | SQLException ex) {
            FrmAlert alert = new FrmAlert("Có lỗi xảy ra : " + ex.getMessage());
            alert.setVisible(true);
            ex.printStackTrace();
        }
    }

    public ResultSet getTable(String tableName) throws SQLException {
        ResultSet rs = null;
        Statement stmt = conn.createStatement();
        rs = stmt.executeQuery("SELECT * FROM " + tableName);
        return rs;
    }
